package com.rezaul.foodrushseller.models;

public class User {

    private Long id;
    private String name;
    private String phone;
    private String businessName;
    private String role;

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getPhone() {
        return phone;
    }

    public String getBusinessName() {
        return businessName;
    }

    public String getRole() {
        return role;
    }
}
